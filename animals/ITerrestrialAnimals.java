package animals;

/**
 * An interface for terrestrial animals that have legs.
 */
public interface ITerrestrialAnimals {
    /**
     * Gets the number of legs the terrestrial animal has.
     *
     * @return The number of legs.
     */
    int getNumberLegs();

    /**
     * Sets the number of legs the terrestrial animal has.
     *
     * @param numberLegs The number of legs to set.
     */
    void setNumberLegs(int numberLegs);
}
